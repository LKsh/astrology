package jyotish.main;

public class IndividualStar {

	private int starNo;
	private int yoniAnimal;
	private int rasi;

	public int getStarNo() {
		return starNo;
	}

	public void setStarNo(int starNo) {
		this.starNo = starNo;
	}

	public int getYoniAnimal() {
		return yoniAnimal;
	}

	public void setYoniAnimal(int yoniAnimal) {
		this.yoniAnimal = yoniAnimal;
	}

	public int getRasi() {
		return rasi;
	}

	public void setRasi(int rasi) {
		rasi = rasi;
	}
}
